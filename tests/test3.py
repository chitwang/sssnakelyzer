# Test case with various operators in Python

# Arithmetic operators
num1 = 10
num2 = 3
result_addition = num1 + num2
result_subtraction = num1 - num2
result_multiplication = num1 * num2
result_division = num1 / num2
result_floor_division = num1 // num2
result_modulo = num1 % num2
result_exponentiation = num1 ** num2

print("Arithmetic Operators:")
print("Addition:", result_addition)
print("Subtraction:", result_subtraction)
print("Multiplication:", result_multiplication)
print("Division:", result_division)
print("Floor Division:", result_floor_division)
print("Modulo:", result_modulo)
print("Exponentiation:", result_exponentiation)

# Comparison operators
num3 = 10
num4 = 20
result_equal = num3 == num4
result_not_equal = num3 != num4
result_greater_than = num3 > num4
result_less_than = num3 < num4
result_greater_than_equal = num3 >= num4
result_less_than_equal = num3 <= num4

print("\nComparison Operators:")
print("Equal:", result_equal)
print("Not Equal:", result_not_equal)
print("Greater Than:", result_greater_than)
print("Less Than:", result_less_than)
print("Greater Than or Equal:", result_greater_than_equal)
print("Less Than or Equal:", result_less_than_equal)

# Logical operators
bool1 = True
bool2 = False
result_logical_and = bool1 and bool2
result_logical_or = bool1 or bool2
result_logical_not = not bool1

print("\nLogical Operators:")
print("Logical AND:", result_logical_and)
print("Logical OR:", result_logical_or)
print("Logical NOT:", result_logical_not)

# Bitwise operators
num5 = 5
num6 = 3
result_bitwise_and = num5 & num6
result_bitwise_or = num5 | num6
result_bitwise_xor = num5 ^ num6
result_bitwise_not = ~num5
result_left_shift = num5 << 1
result_right_shift = num5 >> 1

print("\nBitwise Operators:")
print("Bitwise AND:", result_bitwise_and)
print("Bitwise OR:", result_bitwise_or)
print("Bitwise XOR:", result_bitwise_xor)
print("Bitwise NOT:", result_bitwise_not)
print("Left Shift:", result_left_shift)
print("Right Shift:", result_right_shift)

